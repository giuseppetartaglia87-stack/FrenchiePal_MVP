-- FrenchiePal V2
-- Schema tracking + dashboard KPI per Supabase/Postgres
-- Eseguire questo script nel SQL Editor di Supabase

begin;

create extension if not exists pgcrypto;

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

create table if not exists public.landing_sessions (
  id bigint generated by default as identity primary key,
  session_id text not null unique,
  landing_version text not null default 'v2',
  device_type text not null check (device_type in ('mobile', 'desktop')),
  started_at timestamptz not null default timezone('utc', now()),
  ended_at timestamptz null,
  lead_captured boolean not null default false,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.landing_events (
  id bigint generated by default as identity primary key,
  session_id text not null,
  event_name text not null,
  source_section text null,
  device_type text not null check (device_type in ('mobile', 'desktop')),
  landing_version text not null default 'v2',
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.landing_leads (
  id bigint generated by default as identity primary key,
  session_id text null,
  email text not null,
  lead_source text not null check (lead_source in ('form', 'chat')),
  landing_version text not null default 'v2',
  feature_priority text null,
  experience_rating integer null check (experience_rating between 1 and 5),
  assistant_topic text null,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.chat_sessions (
  id bigint generated by default as identity primary key,
  chat_session_id text not null unique,
  session_id text not null,
  started_at timestamptz not null default timezone('utc', now()),
  ended_at timestamptz null,
  device_type text not null check (device_type in ('mobile', 'desktop')),
  entry_point text not null default 'assistant',
  email_captured boolean not null default false,
  feature_priority text null,
  experience_rating integer null check (experience_rating between 1 and 5),
  inferred_topic text null,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.chat_messages (
  id bigint generated by default as identity primary key,
  chat_session_id text not null,
  role text not null check (role in ('user', 'assistant')),
  message_text text not null,
  message_order integer not null,
  created_at timestamptz not null default timezone('utc', now()),
  unique (chat_session_id, message_order)
);

alter table public.landing_events
  alter column session_id set not null;

alter table public.landing_leads
  add column if not exists landing_version text;

update public.landing_leads
set landing_version = 'v2'
where landing_version is null;

alter table public.landing_leads
  alter column landing_version set default 'v2';

alter table public.landing_leads
  alter column landing_version set not null;

create index if not exists idx_landing_sessions_started_at
  on public.landing_sessions (started_at desc);

create index if not exists idx_landing_sessions_device_type
  on public.landing_sessions (device_type);

create index if not exists idx_landing_events_name_created
  on public.landing_events (event_name, created_at desc);

create index if not exists idx_landing_events_session
  on public.landing_events (session_id);

create index if not exists idx_landing_events_version_created
  on public.landing_events (landing_version, created_at desc);

create index if not exists idx_landing_events_metadata_gin
  on public.landing_events using gin (metadata);

create index if not exists idx_landing_leads_source_created
  on public.landing_leads (lead_source, created_at desc);

create index if not exists idx_landing_leads_session
  on public.landing_leads (session_id);

create index if not exists idx_landing_leads_version_created
  on public.landing_leads (landing_version, created_at desc);

drop index if exists public.idx_landing_leads_email_source;

create index if not exists idx_landing_leads_email
  on public.landing_leads (lower(email));

create index if not exists idx_chat_sessions_session_id
  on public.chat_sessions (session_id);

create index if not exists idx_chat_sessions_started_at
  on public.chat_sessions (started_at desc);

create index if not exists idx_chat_messages_chat_session_order
  on public.chat_messages (chat_session_id, message_order);

create index if not exists idx_chat_messages_created_at
  on public.chat_messages (created_at desc);

